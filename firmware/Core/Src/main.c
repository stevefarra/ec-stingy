/* USER CODE BEGIN 4 */
float movavg(float curr_val, float x[], const short x_idx, const unsigned short window_radius) {
    const unsigned short R = window_radius, W = 2*R + 1;
    const short i = x_idx, j = x_idx - (R + 1);

    if (i <= W) {
        curr_val += x[i] / W;
    } else {
        curr_val = curr_val + (x[j + R] - x[j - (R + 1)]) / W;
    }
    return curr_val;
}
